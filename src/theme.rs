use ratatui::style::Color;

#[derive(Debug, Clone)]
pub struct Theme {
    pub id: &'static str,
    pub label: &'static str,
    pub bg: Color,
    pub surface: Color,
    pub border: Color,
    pub text: Color,
    pub muted: Color,
    pub accent: Color,
    pub success: Color,
    pub warning: Color,
    pub danger: Color,
    pub highlight_bg: Color,
}

pub fn get(id: &str) -> Theme {
    all()
        .into_iter()
        .find(|theme| theme.id == id)
        .unwrap_or_else(|| all()[0].clone())
}

